 <b>СФЕРИЧЕСКИЕ И АСФЕРИЧЕСКИЕ ЛИНЗЫ </b>
</p>
 <b> </b>
<p>
	 Различают два типа линз – сферические и асферические.
</p>
<p>
	 Первый тип более традиционный, используется чаще, и его можно использовать почти при любых рецептах. Радиус кривизны такой линзы одинаков по всей поверхности, то есть она не утолщается и не утончается от периферии к краям.
</p>
<p>
	 Минус сферических линз в том, что при сильной близорукости или дальнозоркости они визуально уменьшают, либо увеличивают глаза. Кроме того, сферические линзы искажают изображение из-за того, что у них по периферии больше диоптрий.
</p>
<p>
	 Врачи чаще рекомендуют асферические линзы. Они гораздо тоньше, не искажают ни изображение, ни сами глаза. Кроме того, их можно вставить в любую оправу, которая понравится. Единственный минус – асферические линзы могут сильно отражать блики, поэтому для таких очков требуется антибликовое покрытие.
</p>
