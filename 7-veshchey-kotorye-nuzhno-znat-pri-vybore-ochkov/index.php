 <b>КОЭФФИЦИЕНТ ПРЕЛОМЛЕНИЯ ЛИНЗЫ</b>
</p>
<p>
	 Немного профессиональной терминологии. Главным параметром очковых линз является коэффициент преломления (или индекс). Чем выше индекс, тем линза тоньше, искривление – меньше. Соответственно, чем более сильные линзы вам нужны, тем разница будет заметнее. Линза с высоким индексом легче, она меньше искажает лицо и глаза и оправу к ней подобрать проще. Если индекс линзы ближе к 1,5, он считается низким, если выше 1,6 – высоким.
